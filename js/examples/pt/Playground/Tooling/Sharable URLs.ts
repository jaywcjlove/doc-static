// Com o novo Playground, os URLs tem muito mais informações
// sobre suas configurações. Isso faz com que reproduções
// sejam precisas, e que você não tenha que falar para alguém
// usar uma determinada flag de compilador para replicar a
// sua configuração.

// A URL contem:
//
// - Se você está em um exemplo
// - O código-fonte compactado para o seu TypeScript/JavaScript
// - Retrocompatilibilidade com URLs antigas não compactadas
// - Em qual idioma você está trabalhando
// - A diferença entre suas configurações de compilação e 
//   a configuração padrão
//   Por exemplo: example:new-compiler-defaults
// - A sua seleção de texto, caso você tenha algum código destacado
//
// O URL é automaticamente atualizado quando ocorre alguma mudança
// nos tópicos acima, utilizando o replaceState do HTML5, então
// o seu botão de voltar vai continuar funcionando como esperado.

// Quando um exemplo é carregado, você ocasionalmente verá e=[numero]
// que é usado para garantir que o navegador recarregue corretamente
// quando você alternar entre os exemplos. É inofensivo.

// Você pode ver a mudança do URL nessa pagina editando alguns dos
// comentários, ou selecionando algum texto. Ao recarregar a página
// o seu navegador vai retornar ao mesmo estado em que estava.
