//// { order: 0, compiler: { target: 1 } }

// TypeScript 플레이그라운드에 오신 것을 환영합니다.
// 이곳은 TypeScript를 웹 브라우저에서 실행하는 환경과 거의 비슷합니다.

// 플레이그라운드에서 간단하게
// 프로젝트를 공유할 수 있기 때문에, TypeScript로 아이디어를
// 쉽고 안전하게 실험할 수 있습니다. 이 페이지의 URL만
// 다른 사람에게 공유하면 됩니다.

const hello = "Hello";

// 오른쪽에서 TypeScript 컴파일러의 결과를 볼 수
// 있습니다. 결과는 브라우저나 서버 및 어디에서나
// 실제로 실행할 수 있는 vanilla JavaScript입니다.

const world = "World";

// "const"를 "var"로 변환하면 코드에서 작은 변화를 
// 확인할 수 있습니다. 이것은 TypeScript를
// 모든 JavaScript 환경에서 실행 가능하도록
// 수행하는 많은 작업 중 하나입니다.

console.log(hello + " " + world);

// 이제 플레이그라운드가 어떻게 동작하는지 알게 되었으니,
// TypeScript가 JavaScript 작업을 더 즐겁게 만드는 방법을 알아봅시다.
// 여기서는 어떻게 기존 지식을 재사용할 수 있는지 
// 보여주기 위해, 가능한 한 vanilla JavaScript에 
// 가깝게 유지하겠습니다.
//
// 계속하려면 아래를 클릭하세요:
//
// example:objects-and-arrays
